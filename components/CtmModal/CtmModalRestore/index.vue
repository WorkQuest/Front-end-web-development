<template>
  <ctm-modal-box
    class="restore"
    :title="$t('forgot.title')"
  >
    <div class="restore__content content">
      <validation-observer v-slot="{handleSubmit, validated, passed, invalid}">
        <div class="content__field">
          {{ $t('forgot.desc') }}
        </div>
        <base-field
          v-model="emailInput"
          :name="$t('placeholders.email')"
          :placeholder="$t('placeholders.email')"
          rules="required|email"
          class="content__input"
          @enter="handleSubmit(restore)"
        />
        <base-btn
          class="content__action"
          :disabled="!validated || !passed || invalid"
          selector="SEND"
          @click="handleSubmit(restore)"
        >
          {{ $t('meta.send') }}
        </base-btn>
      </validation-observer>
    </div>
  </ctm-modal-box>
</template>

<script>
import { mapGetters } from 'vuex';
import modals from '~/store/modals/modals';

export default {
  name: 'ModalRestore',
  data() {
    return {
      emailInput: '',
    };
  },
  computed: {
    ...mapGetters({
      options: 'modals/getOptions',
    }),
  },
  methods: {
    hide() {
      this.CloseModal();
    },
    async restore() {
      const payload = {
        email: this.emailInput,
      };
      try {
        const response = await this.$store.dispatch('user/passwordSendCode', payload);
        if (response?.ok) {
          this.ShowModal({
            key: modals.emailConfirm,
          });
        }
      } catch (e) {
        console.log(e);
        await this.$store.dispatch('main/showToast', {
          title: this.$t('toasts.error'),
          variant: 'warning',
          text: e.response?.data?.msg,
        });
      }
    },
  },
};
</script>

